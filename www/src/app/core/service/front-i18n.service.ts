@Injectable({ providedIn: 'root' })
export class FrontI18nService {
  private lang = 'fr';

  constructor(private languageService: LanguageService) {
    this.lang = this.languageService.getCurrentLanguage();
    this.languageService.getLanguage$().subscribe(l => { this.lang = l; });
  }

  t(key: string): string {
    return DICT[this.lang]?.[key] ?? DICT['fr']?.[key] ?? key;
  }

  get currentLang(): string { return this.lang; }
}
